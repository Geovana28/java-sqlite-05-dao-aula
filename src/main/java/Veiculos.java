
public class Veiculos {
    private int id;
    private String marca;
    private String modelo;
    private String numeroChassi;
    private String placa;
    private String cor;


    public Usuario(){}

    public int getId() {return this.id;}
    public void setId(int id) {this.id = id;}

    public String getmarca() {return this.marca;}
    public void setmarca(String marca) {this.marca = marca;}

    public String getmodelo() {return this.modelo;}
    public void setmodelo(String modelo) {this.modelo = modelo;}

    public String getnumeroChassi() {return this.numeroChassi;}
    public void setnumeroChassi(String numeroChassi) {this.numeroChassi = numeroChassi;}

    public int getplaca() {return this.placa;}
    public void setplaca(int placa) {this.placa = placa;}

    public int getcor() {return this.cor;}
    public void setcor(int cor) {this.cor = cor;}

}